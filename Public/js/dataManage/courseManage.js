$(function () {
    $('#data').attr('class', 'active');
    $('#data_sub').css('display', 'block');
    $('#data_course').attr('class', 'active');


    var i = 1;
    var result = setInterval(function () {
        if (i <= 100) {
            //var percent = i / 100.0;
            $('#progress').css('width', i + "%");
            $('#show_progress').text(i++);
        } else {
            clearInterval(result);
        }
    }, 1000);
});

